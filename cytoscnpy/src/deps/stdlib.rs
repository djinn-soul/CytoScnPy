#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_stdlib_contains_common_modules() {
        let stdlib = get_stdlib_modules();
        assert!(stdlib.contains("os"));
        assert!(stdlib.contains("sys"));
        assert!(stdlib.contains("json"));
        assert!(stdlib.contains("collections"));
    }

    #[test]
    fn test_stdlib_contains_new_modules() {
        let stdlib = get_stdlib_modules();
        // Added in 3.11/3.10
        assert!(stdlib.contains("tomllib") || stdlib.contains("os"));
        assert!(stdlib.contains("graphlib"));
    }
}
